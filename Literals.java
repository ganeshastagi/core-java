public class Literals {
    public static void main(String[] args) {

        // Literals in Java are fixed values that are assigned to variables. They represent constant values directly in code.

        int x = 5;
        float y = 3.14f;
        char c = 'A';
        String s = "Hello";
        boolean b = true;

        // They are literally the values.
    }
}
