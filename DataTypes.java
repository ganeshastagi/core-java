public class DataTypes {
    public static void main(String[] args) {
        //Integers
        byte a = -128;          // 1 byte - 8 bits - Range = -128 to 127
        short b = 32767;        // 2 bytes - 16 bits - Range = -32768 to 32767
        int c = 40000;          // 4 bytes - 32 bits - Range = -2147483648 to 2147833647
        long d = 123456789L;    // 8 bytes - 64 bits - Range = 19 digits

        //Decimal
        float f = 3.14f;        // 4 bytes - 32 bits
        double g = 3.14159265358979323846;      // 8 bytes - 64 bits

        //Character
        char h = 'a';           // 2 bytes - 16 bits

        //Boolean
        boolean i = true;       // True or False
    }
}
